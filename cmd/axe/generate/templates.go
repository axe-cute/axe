var wsPkgClientTmpl = `package ws

import (
	"context"
	"fmt"
	"log/slog"
	"sync"
	"time"

	"github.com/google/uuid"
	"nhooyr.io/websocket"
)

const (
	sendBufSize  = 256
	writeTimeout = 10 * time.Second
	pingInterval = 30 * time.Second
)

// MessageHandler is called by the client when a message is received from the browser.
type MessageHandler func(msg []byte)

// Client wraps an nhooyr.io/websocket connection.
type Client struct {
	id     string
	UserID string

	conn   *websocket.Conn
	hub    *Hub
	sendCh chan []byte
	done   chan struct{} // closed when readPump exits; signals handler to return

	onMessage MessageHandler

	rooms map[string]struct{}
	mu    sync.RWMutex

	log *slog.Logger
}

func newClient(conn *websocket.Conn, hub *Hub) *Client {
	return newClientWithMeta(conn, hub, "")
}

func newClientWithMeta(conn *websocket.Conn, hub *Hub, userID string) *Client {
	return &Client{
		id:     uuid.New().String(),
		UserID: userID,
		conn:   conn,
		hub:    hub,
		sendCh: make(chan []byte, sendBufSize),
		done:   make(chan struct{}),
		rooms:  make(map[string]struct{}),
		log:    hub.log,
	}
}

// ID returns the unique identifier for this client.
func (c *Client) ID() string { return c.id }

// OnMessage registers a handler called for every message received from the peer.
func (c *Client) OnMessage(fn MessageHandler) {
	c.mu.Lock()
	c.onMessage = fn
	c.mu.Unlock()
}

func (c *Client) send(msg []byte) {
	select {
	case c.sendCh <- msg:
		wsMessagesTotal.WithLabelValues("outbound").Inc()
	default:
		c.log.Warn("ws: client send buffer full, dropping message", "client_id", c.id)
	}
}

// Close signals the client to disconnect gracefully.
func (c *Client) Close() {
	close(c.sendCh)
}

// Done returns a channel closed when the client disconnects.
// HTTP handlers should block on <-client.Done() to keep the WebSocket
// connection alive: nhooyr.io/websocket closes the connection when
// the handler's goroutine returns.
func (c *Client) Done() <-chan struct{} { return c.done }

func (c *Client) readPump(ctx context.Context) {
	defer func() {
		close(c.done)          // unblock handler goroutine
		c.hub.unregister <- c
	}()

	for {
		_, msg, err := c.conn.Read(ctx)
		if err != nil {
			if ctx.Err() == nil {
				c.log.Debug("ws: read error", "client_id", c.id, "error", err)
			}
			return
		}
		wsMessagesTotal.WithLabelValues("inbound").Inc()

		c.mu.RLock()
		handler := c.onMessage
		c.mu.RUnlock()

		if handler != nil {
			handler(msg)
		}
	}
}

func (c *Client) writePump(ctx context.Context) {
	ticker := time.NewTicker(pingInterval)
	defer ticker.Stop()

	for {
		select {
		case msg, ok := <-c.sendCh:
			if !ok {
				_ = c.conn.Close(websocket.StatusNormalClosure, "")
				return
			}
			writeCtx, cancel := context.WithTimeout(ctx, writeTimeout)
			err := c.conn.Write(writeCtx, websocket.MessageText, msg)
			cancel()
			if err != nil {
				c.log.Debug("ws: write error", "client_id", c.id, "error", err)
				return
			}

		case <-ticker.C:
			pingCtx, cancel := context.WithTimeout(ctx, writeTimeout)
			err := c.conn.Ping(pingCtx)
			cancel()
			if err != nil {
				c.log.Debug("ws: ping error", "client_id", c.id, "error", err)
				return
			}

		case <-ctx.Done():
			return
		}
	}
}

func (c *Client) joinRoom(roomID string) {
	c.mu.Lock()
	c.rooms[roomID] = struct{}{}
	c.mu.Unlock()
}

func (c *Client) leaveRoom(roomID string) {
	c.mu.Lock()
	delete(c.rooms, roomID)
	c.mu.Unlock()
}

func (c *Client) allRooms() []string {
	c.mu.RLock()
	defer c.mu.RUnlock()
	rooms := make([]string, 0, len(c.rooms))
	for id := range c.rooms {
		rooms = append(rooms, id)
	}
	return rooms
}

// String implements fmt.Stringer.
func (c *Client) String() string {
	return fmt.Sprintf("Client(%s)", c.id)
}
`

